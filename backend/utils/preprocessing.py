def clean_text(text):
    """
    Cleans and preprocesses raw input text:
    1. Converts text to lowercase.
    2. Removes all special characters, punctuation, and numbers.
    3. Tokenizes the text by splitting on whitespace.
    4. Removes common stopwords.
    5. Rejoins tokens into a single clean string.
    """
    if not text or not isinstance(text, str):
        return ""
    
    # 1. Lowercase
    text = text.lower()
    
    # 2. Keep only letters and spaces (remove numbers, punctuation, special chars)
    text = re.sub(r'[^a-z\s]', '', text)
    
    # 3. Tokenize by whitespace
    tokens = text.split()
    
    # 4. Remove stopwords
    filtered_tokens = [word for word in tokens if word not in STOPWORDS]
    
    # 5. Rejoin
    return " ".join(filtered_tokens)
